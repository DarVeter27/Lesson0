name = ('Alexandr')
print('Hello, ' + name)
print('Hello, ''Alexandr')
print(('Hello, ''Alexandr; ') * 5)
print(name[0]) # вывод символа строки с индексом 0
print(name[-1])
print(name[0:4]) # вывод символов строки в диапазоне индексов от 0 до 4 (не включая 4)
print(name[0:4:2]) # вывод символов строки в диапазоне индексов от 0 до 4 с шагом в 2
print(name[:4])
print(name[3:])
print(name[::-1])